# Voor elk taalbestand
Get-ChildItem db/i18n/messages_*.sql | Where-Object { $_.Name -notlike '*en*' } | ForEach-Object {
    $lang = $_.BaseName -replace 'messages_', ''
    $content = Get-Content $_.FullName
    $newContent = @()
    
    # Vind de regel voor commit
    $commitIndex = -1
    for ($i = 0; $i -lt $content.Length; $i++) {
        if ($content[$i] -match "commit;") {
            $commitIndex = $i
            break
        }
    }
    
    if ($commitIndex -gt 0) {
        # Voeg labels toe vlak voor commit
        $newContent += $content[0..($commitIndex-1)]
        
        # Voeg nieuwe labels toe
        $newContent += "  -- Form page labels"
        foreach ($key in $labels[$lang].Keys) {
            if ($key -like "*FORM*") {
                $formKey = $key -replace "_FORM", ""
                $newContent += "  HD_I18N_INSTALL.upsert('SCAFF.MR.FORM.$formKey', '$lang', '$($labels[$lang][$key])');"
            }
        }
        $newContent += ""
        $newContent += "  -- List page labels"
        foreach ($key in $labels[$lang].Keys) {
            if ($key -like "*LIST*") {
                $listKey = $key -replace "_LIST", ""
                $newContent += "  HD_I18N_INSTALL.upsert('SCAFF.MR.LIST.$listKey', '$lang', '$($labels[$lang][$key])');"
            }
        }
        $newContent += ""
        $newContent += $content[$commitIndex..($content.Length-1)]
        Set-Content $_.FullName $newContent
        Write-Host "Updated $($_.Name)"
    }
}